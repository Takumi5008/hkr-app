export const STAGE_CLEAR_REWARDS: string[] = [
  'elf', 'phoenix', 'fairy', 'unicorn', 'knight', 'mermaid', 'witch', 'golem', 'angel', 'wizard',
]

export function getCharStageIndex(totalOpenings: number): number {
  for (let i = CHAR_STAGE_THRESHOLDS.length - 1; i >= 0; i--) {
    if (totalOpenings >= CHAR_STAGE_THRESHOLDS[i]) return i
  }
  return 0
}

